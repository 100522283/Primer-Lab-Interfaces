export function cambiar_idioma(idioma) {
    const t = traducciones[idioma];
    if (!t) return;

    localStorage.setItem('idioma_seleccionado', idioma);
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            if (element.tagName === 'INPUT' && (element.type === 'submit' || element.type === 'button')) {
                element.value = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            element.placeholder = t[key];
        }
    });

    const selector = document.getElementById('idioma');
    if (selector && selector.value !== idioma) {
        selector.value = idioma;
    }

    const banderaActual = document.getElementById('bandera-actual');
    if (banderaActual) {
        if (idioma === 'ES') banderaActual.textContent = 'üá™üá∏';
        else if (idioma === 'EN') banderaActual.textContent = 'üá¨üáß';
    }
}

