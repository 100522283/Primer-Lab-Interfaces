export function cambiar_idioma(idioma) {
    const t = traducciones[idioma];
    if (!t) return;

    localStorage.setItem('idioma_seleccionado', idioma);
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            if (element.tagName === 'INPUT' && (element.type === 'submit' || element.type === 'button')) {
                element.value = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            element.placeholder = t[key];
        }
    });

    const selector = document.getElementById('idioma');
    if (selector && selector.value !== idioma) {
        selector.value = idioma;
    }

    const banderaActual = document.getElementById('bandera-actual');
    if (banderaActual) {
        if (idioma === 'ES') banderaActual.textContent = 'ðŸ‡ªðŸ‡¸';
        else if (idioma === 'EN') banderaActual.textContent = 'ðŸ‡¬ðŸ‡§';
    }
}

export function inicializar_pagina() {

    const idiomaGuardado = localStorage.getItem('idioma_seleccionado') || 'ES';
    cambiar_idioma(idiomaGuardado);

    const usuario = localStorage.getItem("usuario_logueado");

    if (usuario) {
        const btnLoginHeader = document.querySelector('.tipo2 a[data-i18n="login"]');
        if (btnLoginHeader) {
            btnLoginHeader.style.display = 'none';
        }
    }
}